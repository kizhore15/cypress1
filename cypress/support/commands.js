Cypress.Commands.add('addingProductToCart',(inventery)=>{
    cy.get('.inventory_list .inventory_item').each(($prod)=>{
        if($prod.text().includes(inventery)){
            cy.wrap($prod).find('button[class="btn_primary btn_inventory"]').click()
        }
    })
})
